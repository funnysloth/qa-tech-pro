function findValueByKey(companyName, companiesList=company.clients) {
    let nestedPartners = []
    for(let company of companiesList) {
        if(company.name === companyName) return JSON.stringify(company, null, 4)
        if (company.partners && Array.isArray(company.partners)){
            nestedPartners = nestedPartners.concat(company.partners)
        }
    }
    if(nestedPartners.length === 0) return 'No company with such a anme was found'
    return findValueByKey(companyName, nestedPartners)
};

